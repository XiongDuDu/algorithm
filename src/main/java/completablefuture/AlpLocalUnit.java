package completablefuture;

public class AlpLocalUnit {
    /**
     * 计划id
     */
    private long planId;
    /**
     * 单元id
     */
    private Long unitId;
    /**
     * 商品id
     */
    private long gid;

    public long getPlanId() {
        return planId;
    }

    public void setPlanId(long planId) {
        this.planId = planId;
    }

    public long getUnitId() {
        return unitId;
    }

    public void setUnitId(long unitId) {
        this.unitId = unitId;
    }

    public long getGid() {
        return gid;
    }

    public void setGid(long gid) {
        this.gid = gid;
    }
}
